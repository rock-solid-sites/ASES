#!/usr/bin/env python3
"""
validate_evaluation.py — Tier 1 structural validator for EDASES
harness-evaluations and methodology-reviews files.

Implements the EDASES Verification Layer mechanisms proposed in response
to the post-commit adversarial review of issue #3 (Microsoft AutoGen
evaluation). The linter is intentionally a *warning-grade* tool: it
prints structured warnings and exits 0 by default. Use --strict to exit
non-zero on any warning, suitable for CI.

Checks (warning grade, not failure):

  C1  Required sections present per document type (harness-evaluations
      needs sections 1-6; methodology-reviews needs 1-6).
  C2  Heuristic-Scouting-using files name all 4 steps in the header
      (Scout / Target / Verify / Synthesize).
  C3  Every **FIN-NN:** cites at least one OBS-NN.
  C4  Every **OBS-NN:** has a (Source: <URL or file:line>) annotation.
  C5  Source URLs are specific (no bare repo roots like
      https://github.com/owner/repo without a path).
  C6  OBS cited by a FIN (high-stakes) must NOT be tagged
      [per-subagent] — high-stakes claims require direct Orchestrator
      verification.

Auto-generates an evidence trace table (FIN ↔ OBS ↔ Source) with
--emit-trace, written to <file>.trace.md. The trace is a *build
artifact* generated by the linter, not authored by hand (per the
proposal to avoid double-entry bookkeeping).

Usage:
    scripts/validate_evaluation.py path/to/file.md [path/to/other.md ...]
    scripts/validate_evaluation.py --strict path/to/file.md
    scripts/validate_evaluation.py --emit-trace path/to/file.md

Exit codes:
    0 — no warnings, or warnings but --strict not set
    1 — warnings present AND --strict set
    2 — invalid invocation (file not found, etc.)
"""

from __future__ import annotations

import argparse
import re
import sys
from collections import OrderedDict
from pathlib import Path


# ---------------------------------------------------------------------------
# Patterns
# ---------------------------------------------------------------------------

FIN_PATTERN = re.compile(r"\*\*FIN-(\d+)[^*]*:\*\*")
OBS_PATTERN = re.compile(r"\*\*OBS-(\d+):\*\*")
OBS_REF_PATTERN = re.compile(r"\bOBS-(\d+)\b")
SOURCE_PATTERN = re.compile(r"\(Source[s]?:\s*([^)]+)\)")
STATUS_TAG_PATTERN = re.compile(
    r"\[(verified-directly|per-subagent|inferred-from-code)([^\]]*)\]"
)
URL_PATTERN = re.compile(r"https?://[^\s,)\]]+")
BARE_REPO_RE = re.compile(r"^https?://github\.com/[^/]+/[^/]+/?$")
HEURISTIC_STEPS = ("Scout", "Target", "Verify", "Synthesize")

REQUIRED_SECTIONS = {
    "harness-evaluations": [
        "## 1. Overview",
        "## 2. Architectural Alignment",
        "## 3. Key Capabilities",
        "## 4. Observations",
        "## 5. Findings",
        "## 6. Gaps",
    ],
    "methodology-reviews": [
        "## 1. Overview",
        "## 2. Core Principles",
        "## 3. Workflow",
        "## 4. Observations",
        "## 5. Findings",
        "## 6. Relevance",
    ],
}


# ---------------------------------------------------------------------------
# Document type detection
# ---------------------------------------------------------------------------


def detect_doc_type(path: Path) -> str:
    """Return 'harness-evaluations', 'methodology-reviews', or 'unknown'."""
    parts = path.parts
    for candidate in REQUIRED_SECTIONS:
        if candidate in parts:
            return candidate
    return "unknown"


# ---------------------------------------------------------------------------
# Parsing
# ---------------------------------------------------------------------------


def parse_observations(content: str) -> "OrderedDict[str, dict]":
    """Return {obs_id: {source, status, line}} preserving doc order."""
    obs: "OrderedDict[str, dict]" = OrderedDict()
    for line in content.splitlines():
        m = OBS_PATTERN.search(line)
        if not m:
            continue
        obs_id = m.group(1)
        source = None
        source_m = SOURCE_PATTERN.search(line)
        if source_m:
            source = source_m.group(1).strip()
        status = None
        status_m = STATUS_TAG_PATTERN.search(line)
        if status_m:
            status = status_m.group(1)
        obs[obs_id] = {
            "source": source,
            "status": status,
            "line": line.strip(),
        }
    return obs


def parse_findings(content: str) -> "OrderedDict[str, list[str]]":
    """Return {fin_id: [obs_ids]} preserving doc order. A FIN is associated
    with every OBS-NN reference on its bullet line (and any continuation
    lines, treated as a single block for simplicity)."""
    fins: "OrderedDict[str, list[str]]" = OrderedDict()
    current_fin: str | None = None
    for line in content.splitlines():
        fin_m = FIN_PATTERN.search(line)
        if fin_m:
            current_fin = fin_m.group(1)
            fins[current_fin] = []
        if current_fin is not None:
            for obs_m in OBS_REF_PATTERN.finditer(line):
                obs_id = obs_m.group(1)
                if obs_id not in fins[current_fin]:
                    fins[current_fin].append(obs_id)
    return fins


# ---------------------------------------------------------------------------
# Checks (each appends to warnings list)
# ---------------------------------------------------------------------------


def check_required_sections(content: str, doc_type: str, warnings: list) -> None:
    if doc_type not in REQUIRED_SECTIONS:
        return
    for section in REQUIRED_SECTIONS[doc_type]:
        if section not in content:
            warnings.append(
                (
                    "missing-section",
                    section,
                    f"Required section '{section}' not found in {doc_type} file",
                )
            )


def check_heuristic_scouting_header(content: str, warnings: list) -> None:
    if "Heuristic Scouting" not in content and "Heuristic-Scouting" not in content:
        return
    for step in HEURISTIC_STEPS:
        if not re.search(rf"\*\*{step}:\*\*", content):
            warnings.append(
                (
                    "missing-scouting-step",
                    step,
                    f"Heuristic Scouting header must name '{step}:' as a bold list item",
                )
            )


def check_fin_obs_links(
    fins: "OrderedDict[str, list[str]]",
    obs: "OrderedDict[str, dict]",
    warnings: list,
) -> None:
    for fin_id, obs_ids in fins.items():
        if not obs_ids:
            warnings.append(
                (
                    "fin-no-obs",
                    f"FIN-{fin_id}",
                    f"FIN-{fin_id} cites no OBS — synthesis with no evidence is the canonical discipline failure",
                )
            )
            continue
        for obs_id in obs_ids:
            if obs_id not in obs:
                warnings.append(
                    (
                        "fin-obs-missing",
                        f"FIN-{fin_id}->OBS-{obs_id}",
                        f"FIN-{fin_id} cites OBS-{obs_id} but no such OBS exists in the document",
                    )
                )


def check_obs_sources(obs: "OrderedDict[str, dict]", warnings: list) -> None:
    for obs_id, data in obs.items():
        if not data["source"]:
            warnings.append(
                (
                    "obs-no-source",
                    f"OBS-{obs_id}",
                    f"OBS-{obs_id} has no (Source: ...) annotation",
                )
            )
            continue
        source = data["source"]
        urls = URL_PATTERN.findall(source)
        # Treat as file:line if it contains a file extension and a path
        # separator, e.g. "memory.html" or "Heuristic-Scouting.md".
        looks_like_file = bool(
            re.search(r"\.[a-z]{2,4}\b", source) and "/" in source
        )
        if not urls and not looks_like_file:
            warnings.append(
                (
                    "obs-source-vague",
                    f"OBS-{obs_id}",
                    f"OBS-{obs_id} source is neither a URL nor a file path: {source[:60]!r}",
                )
            )


def check_citation_specificity(obs: "OrderedDict[str, dict]", warnings: list) -> None:
    """Flag bare repo roots (e.g. https://github.com/owner/repo) on OBS that
    are NOT tagged [verified-directly]. A bare repo root is a valid citation
    when the Orchestrator has confirmed the OBS is on the README; it is
    suspicious when a subagent reported it without a specific path, because
    the subagent may have lazily cited the homepage instead of the source."""
    for obs_id, data in obs.items():
        if not data["source"]:
            continue
        # If the OBS is verified-directly, trust the Orchestrator's
        # judgement that the repo root is the right citation.
        if data.get("status") == "verified-directly":
            continue
        for url in URL_PATTERN.findall(data["source"]):
            url_clean = url.rstrip(".,)]")
            if BARE_REPO_RE.match(url_clean):
                warnings.append(
                    (
                        "citation-too-broad",
                        f"OBS-{obs_id}",
                        f"OBS-{obs_id} cites bare repo root '{url_clean}' without a specific path; cite the README file or a doc section, or upgrade to [verified-directly] if the README is the source",
                    )
                )


def check_high_stakes_status(
    fins: "OrderedDict[str, list[str]]",
    obs: "OrderedDict[str, dict]",
    warnings: list,
) -> None:
    """OBS cited by a FIN (high-stakes) must NOT be tagged [per-subagent]."""
    high_stakes: set[str] = set()
    for fin_id, obs_ids in fins.items():
        for obs_id in obs_ids:
            if obs_id in obs:
                high_stakes.add(obs_id)
    for obs_id in high_stakes:
        status = obs[obs_id].get("status")
        if status == "per-subagent":
            warnings.append(
                (
                    "high-stakes-per-subagent",
                    f"OBS-{obs_id}",
                    f"OBS-{obs_id} is cited by a FIN but tagged [per-subagent] — high-stakes claims must be [verified-directly] or [inferred-from-code]",
                )
            )


# ---------------------------------------------------------------------------
# Evidence trace generation (auto-generated, not authored)
# ---------------------------------------------------------------------------


def emit_evidence_trace(
    path: Path,
    fins: "OrderedDict[str, list[str]]",
    obs: "OrderedDict[str, dict]",
) -> str:
    lines: list[str] = [
        f"# Evidence trace: {path.name}",
        "",
        "Auto-generated by `scripts/validate_evaluation.py --emit-trace`.",
        "Source of truth: the FIN/OBS annotations in the file. Do not hand-edit.",
        "",
        "| FIN | OBS | Source | Verification |",
        "|-----|-----|--------|--------------|",
    ]
    for fin_id, obs_ids in fins.items():
        if not obs_ids:
            lines.append(f"| FIN-{fin_id} | (no OBS) | — | — |")
            continue
        for obs_id in obs_ids:
            data = obs.get(obs_id, {"source": "—", "status": "—"})
            source = data.get("source") or "—"
            if len(source) > 80:
                source = source[:77] + "..."
            status = data.get("status") or "—"
            lines.append(f"| FIN-{fin_id} | OBS-{obs_id} | {source} | [{status}] |")
    lines.append("")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Driver
# ---------------------------------------------------------------------------


def process_file(path: Path) -> tuple[list[tuple[str, str, str]], str]:
    content = path.read_text(encoding="utf-8")
    warnings: list[tuple[str, str, str]] = []
    doc_type = detect_doc_type(path)

    check_required_sections(content, doc_type, warnings)
    check_heuristic_scouting_header(content, warnings)

    obs = parse_observations(content)
    fins = parse_findings(content)

    check_fin_obs_links(fins, obs, warnings)
    check_obs_sources(obs, warnings)
    check_citation_specificity(obs, warnings)
    check_high_stakes_status(fins, obs, warnings)

    trace = emit_evidence_trace(path, fins, obs)
    return warnings, trace


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Tier 1 structural validator for EDASES evaluation files"
    )
    parser.add_argument("files", nargs="+", help="Markdown files to validate")
    parser.add_argument(
        "--strict",
        action="store_true",
        help="Exit 1 if any warnings are present (suitable for CI)",
    )
    parser.add_argument(
        "--emit-trace",
        action="store_true",
        help="Write auto-generated evidence trace to <file>.trace.md",
    )
    parser.add_argument(
        "--quiet",
        action="store_true",
        help="Only print file-level PASS/FAIL summary; suppress per-warning detail",
    )
    args = parser.parse_args()

    total_warnings = 0
    for file_arg in args.files:
        path = Path(file_arg)
        if not path.exists():
            print(f"ERROR: file not found: {path}", file=sys.stderr)
            return 2
        try:
            warnings, trace = process_file(path)
        except Exception as exc:
            print(f"ERROR processing {path}: {exc}", file=sys.stderr)
            return 2

        if not args.quiet:
            print(f"\n=== {path} ===")
        if not warnings:
            if not args.quiet:
                print("  PASS")
            else:
                print(f"PASS  {path}")
        else:
            if not args.quiet:
                for severity, location, msg in warnings:
                    print(f"  [{severity}] {location}: {msg}")
            else:
                for severity, location, _msg in warnings:
                    print(f"WARN  {path}  [{severity}] {location}")
            total_warnings += len(warnings)
        if args.emit_trace:
            trace_path = path.with_suffix(path.suffix + ".trace.md")
            trace_path.write_text(trace, encoding="utf-8")
            if not args.quiet:
                print(f"  -> trace written to {trace_path}")

    if total_warnings > 0 and args.strict:
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
